package ie.tudublin;

// this class for follow
public class Follow {

    public int aCount;
    public String aString;

        public Follow(int count, String string) {
            this.aCount = count;
            this.aString = string;
        }

        public int getCount() {

            return aCount;
        }

        public String getString() {

            return aCount + aString;
        }

       
				
	
    
}
